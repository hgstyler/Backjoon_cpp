// Question 10950, BJ Online Judge
// https://www.acmicpc.net/problem/10950

#include <iostream>
using namespace std;

int main()
{
	int t, a, b;

	cin >> t;

	for (int loop = 1; loop < t + 1; loop++) {
		cin >> a >> b;
		cout << a + b << endl;
	}
	// basic of 'for', https://boycoding.tistory.com/190

	return 0;
} // END
